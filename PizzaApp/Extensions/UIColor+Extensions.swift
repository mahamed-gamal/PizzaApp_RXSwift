import Foundation
import UIKit

extension UIColor {
  @nonobjc class var black64: UIColor {
    return UIColor(white: 0.0, alpha: 0.64)
  }
  @nonobjc class var black40: UIColor {
    return UIColor(white: 0.0, alpha: 0.4)
  }
  @nonobjc class var pinkishRedTwo: UIColor {
    return UIColor(red: 228.0 / 255.0, green: 40.0 / 255.0, blue: 61.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var pinkyRed: UIColor {
    return UIColor(red: 230.0 / 255.0, green: 42.0 / 255.0, blue: 64.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var darkBlueGrey: UIColor {
    return UIColor(red: 11.0 / 255.0, green: 32.0 / 255.0, blue: 49.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var pinkishRed: UIColor {
    return UIColor(red: 229.0 / 255.0, green: 41.0 / 255.0, blue: 62.0 / 255.0, alpha: 1.0)
  }
}
